"use client";

import React, { useEffect, useState } from "react";
import {
  fetchSpecialFillersPageData,
  getResponsiveImageUrls,
} from "@/lib/api/specials/fetchSpecialsRewardsPageComponent"; // Adjust path to your API utilities

interface SpecialsRewardsData {
  heroSection: {
    show: boolean;
    backgroundColor?: string;
    backgroundImage: {
      mobile?: any;
      tablet?: any;
      desktop?: any;
      large?: any;
      alt?: string;
    };
    breadcrumbs: Array<{ label: string; url: string; ariaLabel?: string }>;
    title: string;
    titleColor?: string;
    titleFontSize?: string;
    mediaImage: {
      mobile?: any;
      tablet?: any;
      desktop?: any;
      large?: any;
      alt?: string;
    };
  };
  indexListSection: {
    show: boolean;
    backgroundColor?: string;
    textColor?: string;
    hoverBackgroundColor?: string;
    hoverTextColor?: string;
    navigationItems: Array<{
      label: string;
      url: string;
      ariaLabel?: string;
    }>;
  };
  indexCardsSection: {
    show: boolean;
    backgroundColor?: string;
    cardBackgroundColor?: string;
    cardHoverBackgroundColor?: string;
    titleColor?: string;
    overlayTextColor?: string;
    overlayBackgroundColor?: string;
    cards: Array<{
      title: string;
      url: string;
      ariaLabel?: string;
      overlayText?: string;
      image: {
        mobile?: any;
        tablet?: any;
        desktop?: any;
        alt?: string;
      };
    }>;
  };
  sectionOrder: Array<{ section: string }>;
  globalStyles: {
    containerMaxWidth?: string;
    containerPadding?: string;
    sectionSpacing?: string;
    fontFamily?: string;
    primaryColor?: string;
    secondaryColor?: string;
    textColor?: string;
    linkColor?: string;
    linkHoverColor?: string;
  };
}

const SpecialsRewards: React.FC = () => {
  const [pageData, setPageData] = useState<SpecialsRewardsData | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const fetchData = async () => {
      try {
        const data = await fetchSpecialFillersPageData();
        setPageData(data);
      } catch (error) {
        console.error("Error fetching Specials & Rewards page data:", error);
      } finally {
        setLoading(false);
      }
    };
    fetchData();
  }, []);

  if (loading) return <div>Loading...</div>;
  if (!pageData) return <div>No data available</div>;

  const {
    heroSection,
    indexListSection,
    indexCardsSection,
    sectionOrder,
    globalStyles,
  } = pageData;

  const placeholder =
    "data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNkYAAAAAYAAjCB0C8AAAAASUVORK5CYII=";

  const renderSection = (section: string) => {
    switch (section) {
      case "hero":
        if (!heroSection.show) return null;
        const heroBgImages = getResponsiveImageUrls(
          heroSection.backgroundImage
        );
        const heroMediaImages = getResponsiveImageUrls(heroSection.mediaImage);
        return (
          <div
            className="mod_hero banner mobile-stack"
            data-s3-module
            style={{ backgroundColor: heroSection.backgroundColor }}
          >
            <div className="wrapper">
              <picture className="background">
                <source
                  media="(max-width:420px)"
                  srcSet={placeholder}
                  data-lazyload-srcset={`${heroBgImages.mobile}?auto=format,compress&w=420, ${heroBgImages.mobile}?auto=format,compress&w=630 2x`}
                />
                <source
                  media="(max-width:800px)"
                  srcSet={placeholder}
                  data-lazyload-srcset={`${heroBgImages.tablet}?auto=format,compress&w=800, ${heroBgImages.tablet}?auto=format,compress&w=1200 2x`}
                />
                <source
                  media="(max-width:1400px)"
                  srcSet={placeholder}
                  data-lazyload-srcset={`${heroBgImages.desktop}?auto=format,compress&w=1400, ${heroBgImages.desktop}?auto=format,compress&w=2100 2x`}
                />
                <source
                  srcSet={placeholder}
                  data-lazyload-srcset={`${heroBgImages.large}?auto=format,compress&w=1994`}
                />
                <img
                  src={placeholder}
                  data-lazyload-src={`${heroBgImages.large}?auto=format,compress&w=1994`}
                  loading="lazy"
                  data-lazyload="img"
                  width="1994"
                  height="1206"
                  alt={heroBgImages.alt || "Background Texture"}
                  draggable="false"
                />
              </picture>
              <div className="row">
                <div className="inner">
                  <ul className="partial_breadcrumb" data-s3-partial>
                    {heroSection.breadcrumbs.map((crumb, index) => (
                      <li key={index}>
                        <a
                          href={crumb.url}
                          role="link"
                          aria-label={
                            crumb.ariaLabel ||
                            "Breadcrumbs to help navigate the user"
                          }
                        >
                          {crumb.label}
                        </a>
                      </li>
                    ))}
                  </ul>
                  <h1
                    style={{
                      color: heroSection.titleColor,
                      fontSize: heroSection.titleFontSize,
                    }}
                  >
                    {heroSection.title}
                  </h1>
                </div>
              </div>
              <div className="media">
                <picture className="background">
                  <source
                    media="(max-width:420px)"
                    srcSet={`${heroMediaImages.mobile}?auto=format,compress&w=420, ${heroMediaImages.mobile}?auto=format,compress&w=630 2x`}
                    data-lazyload-srcset={`${heroMediaImages.mobile}?auto=format,compress&w=420, ${heroMediaImages.mobile}?auto=format,compress&w=630 2x`}
                  />
                  <source
                    media="(max-width:800px)"
                    srcSet={`${heroMediaImages.tablet}?auto=format,compress&w=800, ${heroMediaImages.tablet}?auto=format,compress&w=1200 2x`}
                    data-lazyload-srcset={`${heroMediaImages.tablet}?auto=format,compress&w=800, ${heroMediaImages.tablet}?auto=format,compress&w=1200 2x`}
                  />
                  <source
                    media="(max-width:1400px)"
                    srcSet={`${heroMediaImages.desktop}?auto=format,compress&w=1400, ${heroMediaImages.desktop}?auto=format,compress&w=2100 2x`}
                    data-lazyload-srcset={`${heroMediaImages.desktop}?auto=format,compress&w=1400, ${heroMediaImages.desktop}?auto=format,compress&w=2100 2x`}
                  />
                  <source
                    srcSet={`${heroMediaImages.large}?auto=format,compress&w=1920`}
                    data-lazyload-srcset={`${heroMediaImages.large}?auto=format,compress&w=1920`}
                  />
                  <img
                    src={`${heroMediaImages.large}?auto=format,compress&w=1920`}
                    width="1920"
                    height="1206"
                    alt={heroMediaImages.alt || "Goldfingers exam office"}
                    draggable="false"
                  />
                </picture>
              </div>
            </div>
          </div>
        );

      case "indexList":
        if (!indexListSection.show) return null;
        return (
          <div
            className="mod_index_list option-1 container"
            data-s3-module
            style={{
              backgroundColor: indexListSection.backgroundColor,
              color: indexListSection.textColor,
            }}
          >
            <div className="row ">
              <div className="inner">
                {indexListSection.navigationItems.map((item, index) => (
                  <a
                    key={index}
                    href={item.url}
                    className="item"
                    title={item.ariaLabel}
                    aria-label={item.ariaLabel}
                    style={{
                      color: indexListSection.textColor,
                    }}
                    onMouseEnter={(e) => {
                      if (indexListSection.hoverBackgroundColor) {
                        e.currentTarget.style.backgroundColor =
                          indexListSection.hoverBackgroundColor;
                      }
                      if (indexListSection.hoverTextColor) {
                        e.currentTarget.style.color =
                          indexListSection.hoverTextColor;
                      }
                    }}
                    onMouseLeave={(e) => {
                      if (indexListSection.backgroundColor) {
                        e.currentTarget.style.backgroundColor = "";
                      }
                      if (indexListSection.textColor) {
                        e.currentTarget.style.color =
                          indexListSection.textColor;
                      }
                    }}
                  >
                    <span>{item.label}</span>
                  </a>
                ))}
              </div>
            </div>
          </div>
        );

      case "indexCards":
        if (!indexCardsSection.show) return null;
        return (
          <div
            className="mod_index container"
            data-s3-module
            style={{ backgroundColor: indexCardsSection.backgroundColor }}
          >
            <div className="row subcontent ">
              <div className="inner">
                {indexCardsSection.cards.map((card, index) => {
                  const cardImages = getResponsiveImageUrls(card.image);
                  return (
                    <div className="item" key={index}>
                      <a
                        className="media-btn"
                        href={card.url}
                        title={card.ariaLabel}
                        aria-label={card.ariaLabel}
                      >
                        <div className="media">
                          <picture className="background">
                            <source
                              media="(max-width:420px)"
                              srcSet={placeholder}
                              data-lazyload-srcset={`${cardImages.mobile}?auto=format,compress&w=420, ${cardImages.mobile}?auto=format,compress&w=630 2x`}
                            />
                            <source
                              media="(max-width:800px)"
                              srcSet={placeholder}
                              data-lazyload-srcset={`${cardImages.tablet}?auto=format,compress&w=800, ${cardImages.tablet}?auto=format,compress&w=1200 2x`}
                            />
                            <source
                              srcSet={placeholder}
                              data-lazyload-srcset={`${
                                cardImages.desktop || cardImages.large
                              }?auto=format,compress&w=850`}
                            />
                            <img
                              src={placeholder}
                              data-lazyload-src={`${
                                cardImages.desktop || cardImages.large
                              }?auto=format,compress&w=850`}
                              loading="lazy"
                              data-lazyload="img"
                              width="850"
                              height="534"
                              alt={cardImages.alt || card.title}
                              draggable="false"
                            />
                          </picture>
                          <div
                            className="content"
                            style={{
                              color: indexCardsSection.overlayTextColor,
                              backgroundColor:
                                indexCardsSection.overlayBackgroundColor,
                            }}
                          >
                            <span>{card.overlayText || card.title}</span>
                          </div>
                        </div>
                      </a>
                      <div className="copy">
                        <a
                          href={card.url}
                          className="title"
                          title={card.ariaLabel}
                          aria-label={card.ariaLabel}
                          style={{ color: indexCardsSection.titleColor }}
                        >
                          <h2>{card.title}</h2>
                        </a>
                      </div>
                    </div>
                  );
                })}
              </div>
            </div>
          </div>
        );

      default:
        return null;
    }
  };

  return (
    <main
      id="main"
      style={{
        maxWidth: globalStyles.containerMaxWidth,
        padding: globalStyles.containerPadding,
        fontFamily: globalStyles.fontFamily,
        color: globalStyles.textColor,
        margin: "0 auto",
      }}
    >
      {/* Page Layout */}
      {sectionOrder.map((item, index) => (
        <React.Fragment key={index}>
          {renderSection(item.section)}
        </React.Fragment>
      ))}
      {/* /Page Layout */}
    </main>
  );
};

