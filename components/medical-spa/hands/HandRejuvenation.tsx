const HandRejuvenation: React.FC = () => {
  const [pageData, setPageData] = useState<HandRejuvenationData | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const fetchData = async () => {
      try {
        const data = await fetchHandFillersPageData(
          "hand-rejuvenation-page-component"
        ); // Fetch from hand-rejuvenation-page-component
        setPageData(data);
      } catch (error) {
        console.error("Error fetching hand rejuvenation page data:", error);
      } finally {
        setLoading(false);
      }
    };
    fetchData();
  }, []);

  const getLogoUrl = (logo: any) => {
    if (!logo) return null;

    // If logo is a string, return it as is
    if (typeof logo === "string") return logo;

    // Prioritize cloudinary_url if available
    if (logo.cloudinary_url) return logo.cloudinary_url;

    // Fallback to url property
    if (logo.url) return logo.url;

    return null;
  };

  if (loading) return <div>Loading...</div>;
  if (!pageData) return <div>No data available</div>;

  const {
    heroSection,
    introSection,
    bannerSection1,
    columnSection1,
    halfImageSection,
    boxGridSection,
    columnSection2,
    bannerSection2,
    sectionOrder,
    globalStyles,
  } = pageData;

  const renderSection = (section: string) => {
    switch (section) {
      case "hero":
        if (!heroSection.show) return null;
        const heroBgImages = getResponsiveImageUrls(
          heroSection.backgroundImage
        );
        const heroMediaImages = getResponsiveImageUrls(heroSection.mediaImage);
        return (
          <div
            className="mod_hero banner mobile-stack"
            data-s3-module
            style={{ backgroundColor: heroSection.backgroundColor }}
          >
            <div className="wrapper">
              <picture className="background">
                <source
                  media="(max-width:420px)"
                  srcSet={heroBgImages.mobile}
                />
                <source
                  media="(max-width:800px)"
                  srcSet={heroBgImages.tablet}
                />
                <source
                  media="(max-width:1400px)"
                  srcSet={heroBgImages.desktop}
                />
                <source srcSet={heroBgImages.large} />
                <img
                  src={heroBgImages.large}
                  loading="lazy"
                  alt={heroBgImages.alt || "Background Texture"}
                  draggable="false"
                />
              </picture>
              <div className="row">
                <div className="inner">
                  <ul className="partial_breadcrumb" data-s3-partial>
                    {heroSection.breadcrumbs.map((crumb, index) => (
                      <li key={index}>
                        <a
                          href={crumb.url}
                          role="link"
                          aria-label={crumb.ariaLabel}
                        >
                          {crumb.label}
                        </a>
                      </li>
                    ))}
                  </ul>
                  <h1
                    style={{
                      color: heroSection.titleColor,
                      fontSize: heroSection.titleFontSize,
                    }}
                  >
                    {heroSection.title}
                  </h1>
                  <div
                    className="text"
                    style={{ color: heroSection.descriptionColor }}
                  >
                    <div
                      dangerouslySetInnerHTML={{
                        __html: lexicalToHtml(heroSection.description),
                      }}
                    />
                    {heroSection.ctaButton.show && (
                      <p>
                        <a
                          className="btn white"
                          href={heroSection.ctaButton.url}
                          role="button"
                          style={{
                            backgroundColor:
                              heroSection.ctaButton.backgroundColor,
                            color: heroSection.ctaButton.textColor,
                          }}
                          onMouseOver={(e) =>
                            heroSection.ctaButton.hoverBackgroundColor &&
                            (e.currentTarget.style.backgroundColor =
                              heroSection.ctaButton.hoverBackgroundColor)
                          }
                          onMouseOut={(e) =>
                            heroSection.ctaButton.backgroundColor &&
                            (e.currentTarget.style.backgroundColor =
                              heroSection.ctaButton.backgroundColor)
                          }
                        >
                          {heroSection.ctaButton.text}
                        </a>
                      </p>
                    )}
                  </div>
                </div>
              </div>
              <div className="media">
                <picture className="background">
                  <source
                    media="(max-width:420px)"
                    srcSet={heroMediaImages.mobile}
                  />
                  <source
                    media="(max-width:800px)"
                    srcSet={heroMediaImages.tablet}
                  />
                  <source
                    media="(max-width:1400px)"
                    srcSet={heroMediaImages.desktop}
                  />
                  <source srcSet={heroMediaImages.large} />
                  <img
                    src={heroMediaImages.large}
                    loading="lazy"
                    alt={
                      heroMediaImages.alt ||
                      "Doctor going over chart with patient"
                    }
                    draggable="false"
                  />
                </picture>
              </div>
            </div>
          </div>
        );

      case "intro":
        if (!introSection.show) return null;
        return (
          <div
            className="mod_intro container viewport option-1 wide wow fadeInUp"
            data-s3-module
            style={{
              backgroundColor: introSection.backgroundColor,
              color: introSection.textColor,
            }}
          >
            <div className="row">
              {introSection.logo && (
                <picture className="logo">
                  <img
                    // src={getResponsiveImageUrls(introSection.logo).large}
                    src={getLogoUrl(introSection.logo)}
                    loading="lazy"
                    alt={introSection.logoAlt || "Goldfingers aesthetic logo"}
                    draggable="false"
                    data-api-key="logo"
                  />
                </picture>
              )}
              <div className="inner" data-api-key="content">
                <h2 style={{ color: introSection.headingColor }}>
                  {introSection.heading}
                </h2>
                <div
                  dangerouslySetInnerHTML={{
                    __html: lexicalToHtml(introSection.content),
                  }}
                />
              </div>
            </div>
          </div>
        );

      case "bannerSection1":
        if (!bannerSection1.show) return null;
        const banner1Images = getResponsiveImageUrls(
          bannerSection1.backgroundImage
        );
        return (
          <div
            className={`mod_banner dark ${
              bannerSection1.hideDivider ? "hide-divider" : ""
            } viewport background-3`}
            data-s3-module
            style={{
              backgroundColor: bannerSection1.backgroundColor,
              color: bannerSection1.textColor,
            }}
          >
            <div className="row wow fadeInUp">
              <div className="content" data-api-key="top_content">
                <h2 style={{ color: bannerSection1.headingColor }}>
                  {bannerSection1.heading}
                </h2>
                <div
                  dangerouslySetInnerHTML={{
                    __html: lexicalToHtml(bannerSection1.content),
                  }}
                />
              </div>
            </div>
            <picture className="background">
              <source media="(max-width:420px)" srcSet={banner1Images.mobile} />
              <source media="(max-width:800px)" srcSet={banner1Images.tablet} />
              <source
                media="(max-width:1400px)"
                srcSet={banner1Images.desktop}
              />
              <source srcSet={banner1Images.large} />
              <img
                src={banner1Images.large}
                loading="lazy"
                alt={banner1Images.alt || "Banner media"}
                draggable="false"
              />
            </picture>
          </div>
        );

      case "columnSection1":
        if (!columnSection1.show) return null;
        return (
          <div
            className="mod_column viewport container"
            data-s3-module
            style={{
              backgroundColor: columnSection1.backgroundColor,
              color: columnSection1.textColor,
            }}
          >
            <div
              className="row mt-30 mb-30"
              data-api-key="content"
              style={{
                marginTop: columnSection1.marginTop,
                marginBottom: columnSection1.marginBottom,
              }}
            >
              <div className="item">
                <h2 style={{ color: columnSection1.headingColor }}>
                  {columnSection1.heading}
                </h2>
                <div
                  dangerouslySetInnerHTML={{
                    __html: lexicalToHtml(columnSection1.content),
                  }}
                />
                {columnSection1.bulletPoints && (
                  <ul className="columns">
                    {columnSection1.bulletPoints.map((bullet, index) => (
                      <li key={index}>{bullet.text}</li>
                    ))}
                  </ul>
                )}
              </div>
            </div>
          </div>
        );

      case "halfImageSection":
        if (!halfImageSection.show) return null;
        const halfImages = getResponsiveImageUrls(halfImageSection.image);
        return (
          <div
            className={`mod_half_image viewport half-image container wow fadeInLeft background-navy`}
            data-s3-module
            style={{
              backgroundColor: halfImageSection.backgroundColor,
              color: halfImageSection.textColor,
            }}
          >
            <div
              className={`row half-image-row ${
                halfImageSection.imagePosition === "left" ? "" : "flipped"
              } mt-0 mb-0`}
              style={{
                marginTop: halfImageSection.marginTop,
                marginBottom: halfImageSection.marginBottom,
              }}
            >
              <div className="media">
                <picture className="background">
                  <source
                    media="(max-width:420px)"
                    srcSet={halfImages.mobile}
                  />
                  <source
                    media="(max-width:800px)"
                    srcSet={halfImages.tablet}
                  />
                  <source
                    media="(max-width:1400px)"
                    srcSet={halfImages.desktop}
                  />
                  <source srcSet={halfImages.large} />
                  <img
                    src={halfImages.large}
                    loading="lazy"
                    alt={halfImages.alt || "Brunette woman smiling"}
                    draggable="false"
                    data-api-key="image"
                  />
                </picture>
              </div>
              <div className="copy padded">
                <div className="inner">
                  <div className="content" data-api-key="content">
                    <h2 style={{ color: halfImageSection.headingColor }}>
                      {halfImageSection.heading}
                    </h2>
                    <div
                      dangerouslySetInnerHTML={{
                        __html: lexicalToHtml(halfImageSection.content),
                      }}
                    />
                  </div>
                </div>
              </div>
            </div>
          </div>
        );

      case "boxGridSection":
        if (!boxGridSection.show) return null;
        return (
          <div
            className={`mod_box_grid viewport container option-1 ${boxGridSection.gridLayout} no-bg wider pt-20`}
            data-s3-module
            style={{
              backgroundColor: boxGridSection.backgroundColor,
              color: boxGridSection.textColor,
              paddingTop: boxGridSection.paddingTop,
            }}
          >
            <div className="row">
              <div className="content-before">
                <h2 style={{ color: boxGridSection.headingColor }}>
                  {boxGridSection.heading}
                </h2>
                <div
                  dangerouslySetInnerHTML={{
                    __html: lexicalToHtml(boxGridSection.introContent),
                  }}
                />
              </div>
              <div className={`boxes ${boxGridSection.gridLayout}`}>
                {boxGridSection.boxes.map((box, index) => (
                  <div key={index} className="box wow fadeInUp">
                    <h3>{box.title}</h3>
                    <div
                      dangerouslySetInnerHTML={{
                        __html: lexicalToHtml(box.content),
                      }}
                    />
                  </div>
                ))}
              </div>
            </div>
          </div>
        );

      case "columnSection2":
        if (!columnSection2.show) return null;
        const col2Images = getResponsiveImageUrls(columnSection2.mediaImage);
        return (
          <div
            className="mod_column viewport container has-media"
            data-s3-module
            style={{
              backgroundColor: columnSection2.backgroundColor,
              color: columnSection2.textColor,
            }}
          >
            <div
              className={`row pt-0 mb-30 media-${columnSection2.mediaPosition}`}
              data-api-key="content"
              style={{
                paddingTop: columnSection2.paddingTop,
                marginBottom: columnSection2.marginBottom,
              }}
            >
              <div className="item">
                <h2 style={{ color: columnSection2.headingColor }}>
                  {columnSection2.heading}
                </h2>
                <div
                  dangerouslySetInnerHTML={{
                    __html: lexicalToHtml(columnSection2.content),
                  }}
                />
              </div>
              <picture className="media">
                <source media="(max-width:420px)" srcSet={col2Images.mobile} />
                <source media="(max-width:800px)" srcSet={col2Images.tablet} />
                <source srcSet={col2Images.desktop} />
                <img
                  src={col2Images.desktop}
                  loading="lazy"
                  alt={col2Images.alt || "dock on the ocean"}
                  draggable="false"
                />
              </picture>
            </div>
          </div>
        );

      case "bannerSection2":
        if (!bannerSection2.show) return null;
        const banner2Images = getResponsiveImageUrls(
          bannerSection2.backgroundImage
        );
        return (
          <div
            className={`mod_banner dark ${
              bannerSection2.hideDivider ? "hide-divider" : ""
            } viewport background-1`}
            data-s3-module
            style={{
              backgroundColor: bannerSection2.backgroundColor,
              color: bannerSection2.textColor,
            }}
          >
            <div className="row wow fadeInUp">
              <div className="content" data-api-key="top_content">
                <h2 style={{ color: bannerSection2.headingColor }}>
                  {bannerSection2.heading}
                </h2>
                <div
                  dangerouslySetInnerHTML={{
                    __html: lexicalToHtml(bannerSection2.content),
                  }}
                />
                {bannerSection2.bulletPoints && (
                  <ul className="columns">
                    {bannerSection2.bulletPoints.map((bullet, index) => (
                      <li key={index}>{bullet.text}</li>
                    ))}
                  </ul>
                )}
              </div>
            </div>
            <picture className="background">
              <source media="(max-width:420px)" srcSet={banner2Images.mobile} />
              <source media="(max-width:800px)" srcSet={banner2Images.tablet} />
              <source
                media="(max-width:1400px)"
                srcSet={banner2Images.desktop}
              />
              <source srcSet={banner2Images.large} />
              <img
                src={banner2Images.large}
                loading="lazy"
                alt={banner2Images.alt || "Banner media"}
                draggable="false"
              />
            </picture>
          </div>
        );

      default:
        return null;
    }
  };

  return (
    <main
      id="main"
      style={{
        maxWidth: globalStyles.containerMaxWidth,
        padding: globalStyles.containerPadding,
        fontFamily: globalStyles.fontFamily,
        color: globalStyles.textColor,
        margin: "0 auto",
      }}
    >
      {sectionOrder.map((item, index) => (
        <React.Fragment key={index}>
          {renderSection(item.section)}
        </React.Fragment>
      ))}
    </main>
  );
};

export default HandRejuvenation;