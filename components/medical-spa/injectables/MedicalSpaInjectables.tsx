"use client";

import React from "react";
import {
  getResponsiveImageUrls,
  lexicalToHtml,
} from "@/lib/api/medicalSpa/fetchMedicalSpaInjectablesData";

const MedicalSpaInjectablesPage: React.FC<{ data: any }> = ({ data }) => {
  if (!data) {
    return <div>Loading...</div>;
  }

  const {
    heroSection,
    indexStaticSection,
    sectionOrder,
    globalStyles,
    animationSettings,
  } = data;

  // Get responsive image URLs
  const heroBackground = getResponsiveImageUrls(heroSection?.backgroundImage);
  const heroMedia = getResponsiveImageUrls(heroSection?.mediaImage);

  // Helper function to render rich text content
  const renderRichText = (content: any) => {
    if (!content) return null;
    const html = lexicalToHtml(content);
    return <div dangerouslySetInnerHTML={{ __html: html }} />;
  };

  // Render sections based on order
  const renderSection = (sectionKey: string) => {
    switch (sectionKey) {
      case "hero":
        return (
          heroSection?.show && (
            <div
              className="mod_hero banner mobile-stack"
              data-s3-module
              key="hero"
            >
              <div className="wrapper">
                <picture className="background">
                  <source
                    media="(max-width:420px)"
                    srcSet="data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNkYAAAAAYAAjCB0C8AAAAASUVORK5CYII="
                    data-lazyload-srcset={`${heroBackground.mobile}, ${heroBackground.mobile} 2x`}
                  />
                  <source
                    media="(max-width:800px)"
                    srcSet="data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNkYAAAAAYAAjCB0C8AAAAASUVORK5CYII="
                    data-lazyload-srcset={`${heroBackground.tablet}, ${heroBackground.tablet} 2x`}
                  />
                  <source
                    media="(max-width:1400px)"
                    srcSet="data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNkYAAAAAYAAjCB0C8AAAAASUVORK5CYII="
                    data-lazyload-srcset={`${heroBackground.desktop}, ${heroBackground.desktop} 2x`}
                  />
                  <source
                    srcSet="data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNkYAAAAAYAAjCB0C8AAAAASUVORK5CYII="
                    data-lazyload-srcset={heroBackground.large}
                  />
                  <img
                    src="data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNkYAAAAAYAAjCB0C8AAAAASUVORK5CYII="
                    data-lazyload-src={heroBackground.large}
                    loading="lazy"
                    data-lazyload="img"
                    width="1994"
                    height="1206"
                    alt={heroBackground.alt}
                    draggable="false"
                  />
                </picture>
                <div className="row">
                  <div className="inner">
                    {heroSection.breadcrumbs &&
                      heroSection.breadcrumbs.length > 0 && (
                        <ul className="partial_breadcrumb" data-s3-partial>
                          {heroSection.breadcrumbs.map(
                            (crumb: any, idx: number) => (
                              <li key={idx}>
                                <a
                                  href={crumb.url}
                                  role="link"
                                  aria-label={crumb.ariaLabel}
                                >
                                  {crumb.label}
                                </a>
                              </li>
                            )
                          )}
                        </ul>
                      )}
                    <h1
                      style={{
                        color: heroSection.titleColor || undefined,
                        fontSize: heroSection.titleFontSize || undefined,
                      }}
                    >
                      {heroSection.title}
                    </h1>
                  </div>
                </div>
                <div className="media">
                  <picture className="background">
                    <source
                      media="(max-width:420px)"
                      srcSet={`${heroMedia.mobile}, ${heroMedia.mobile} 2x`}
                      data-lazyload-srcset={`${heroMedia.mobile}, ${heroMedia.mobile} 2x`}
                    />
                    <source
                      media="(max-width:800px)"
                      srcSet={`${heroMedia.tablet}, ${heroMedia.tablet} 2x`}
                      data-lazyload-srcset={`${heroMedia.tablet}, ${heroMedia.tablet} 2x`}
                    />
                    <source
                      media="(max-width:1400px)"
                      srcSet={`${heroMedia.desktop}, ${heroMedia.desktop} 2x`}
                      data-lazyload-srcset={`${heroMedia.desktop}, ${heroMedia.desktop} 2x`}
                    />
                    <source
                      srcSet={heroMedia.large}
                      data-lazyload-srcset={heroMedia.large}
                    />
                    <img
                      src={heroMedia.large}
                      width="1920"
                      height="1206"
                      alt={heroMedia.alt}
                      draggable="false"
                    />
                  </picture>
                </div>
              </div>
            </div>
          )
        );

      case "indexStatic":
        return (
          indexStaticSection?.show &&
          indexStaticSection.cards && (
            <div
              className="mod_index_static mod_index container"
              data-s3-module
              key="indexStatic"
              style={{
                backgroundColor:
                  indexStaticSection.backgroundColor || undefined,
              }}
            >
              <div className="row subcontent">
                <div
                  className="inner"
                  style={{
                    backgroundColor:
                      indexStaticSection.containerBackgroundColor || undefined,
                  }}
                >
                  {indexStaticSection.cards.map((card: any, idx: number) => {
                    const cardImage = getResponsiveImageUrls(card.image);

                    return (
                      <div className="item" key={idx}>
                        <a
                          href={card.url}
                          className="media-btn"
                          style={{
                            transition: animationSettings?.enableAnimations
                              ? `all ${
                                  animationSettings.transitionDuration || "0.3s"
                                } ${
                                  animationSettings.transitionEasing ||
                                  "ease-in-out"
                                }`
                              : undefined,
                          }}
                        >
                          <div className="media">
                            <picture className="background">
                              <source
                                media="(max-width:420px)"
                                srcSet="data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNkYAAAAAYAAjCB0C8AAAAASUVORK5CYII="
                                data-lazyload-srcset={`${cardImage.mobile}, ${cardImage.mobile} 2x`}
                              />
                              <source
                                media="(max-width:800px)"
                                srcSet="data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNkYAAAAAYAAjCB0C8AAAAASUVORK5CYII="
                                data-lazyload-srcset={`${cardImage.tablet}, ${cardImage.tablet} 2x`}
                              />
                              <source
                                media="(max-width:1400px)"
                                srcSet="data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNkYAAAAAYAAjCB0C8AAAAASUVORK5CYII="
                                data-lazyload-srcset={`${cardImage.desktop}, ${cardImage.desktop} 2x`}
                              />
                              <source
                                srcSet="data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNkYAAAAAYAAjCB0C8AAAAASUVORK5CYII="
                                data-lazyload-srcset={
                                  cardImage.large || cardImage.desktop
                                }
                              />
                              <img
                                src="data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNkYAAAAAYAAjCB0C8AAAAASUVORK5CYII="
                                data-lazyload-src={
                                  cardImage.large || cardImage.desktop
                                }
                                loading="lazy"
                                data-lazyload="img"
                                width="1920"
                                height="1206"
                                alt={cardImage.alt}
                                draggable="false"
                              />
                            </picture>
                            <div
                              className="content"
                              style={{
                                backgroundColor:
                                  card.customOverlayBackgroundColor ||
                                  indexStaticSection.overlayBackgroundColor ||
                                  undefined,
                                color:
                                  card.customOverlayTextColor ||
                                  indexStaticSection.overlayTextColor ||
                                  undefined,
                              }}
                            >
                              <span>{card.overlayLabel || card.title}</span>
                            </div>
                          </div>
                        </a>
                        <div className="copy">
                          <a
                            href={card.url}
                            className="title"
                            style={{
                              color:
                                card.customTextColor ||
                                indexStaticSection.cardTextColor ||
                                undefined,
                            }}
                          >
                            <h2>{card.title}</h2>
                          </a>
                        </div>
                      </div>
                    );
                  })}
                </div>
              </div>
            </div>
          )
        );

      default:
        return null;
    }
  };

  return (
    <main
      id="main"
      style={{
        fontFamily: globalStyles?.fontFamily || undefined,
        color: globalStyles?.textColor || undefined,
      }}
    >
      {sectionOrder && sectionOrder.length > 0 ? (
        sectionOrder.map((section: any, idx: number) =>
          renderSection(section.section)
        )
      ) : (
        <>
          {renderSection("hero")}
          {renderSection("indexStatic")}
        </>
      )}
    </main>
  );
};

export default MedicalSpaInjectablesPage;