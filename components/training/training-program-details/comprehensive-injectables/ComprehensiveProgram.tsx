"use client";

import React, { useEffect, useState } from "react";
import {
  fetchComprehensiveProgramPageData,
  getResponsiveImageUrls,
  lexicalToHtml,
  getImageUrl,
} from "@/lib/api/traning/fetchComprehensiveProgram";

interface ComprehensiveProgramData {
  componentName?: string;
  introSection?: any;
  durationSection?: any;
  twoColumnSection?: any;
  threeColumnSection?: any;
  boxGridSection?: any;
  tuitionSection?: any;
  sectionOrder?: Array<{ section: string }>;
  globalStyles?: any;
}

const ComprehensiveProgram: React.FC = () => {
  const [pageData, setPageData] = useState<ComprehensiveProgramData | null>(
    null
  );
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const fetchData = async () => {
      try {
        const data = await fetchComprehensiveProgramPageData();
        setPageData(data);
      } catch (error) {
        console.error("Error fetching Comprehensive Program page data:", error);
      } finally {
        setLoading(false);
      }
    };
    fetchData();
  }, []);

  if (loading) return <div>Loading...</div>;
  if (!pageData) return <div>No data available</div>;

  const {
    introSection,
    durationSection,
    twoColumnSection,
    threeColumnSection,
    boxGridSection,
    tuitionSection,
    sectionOrder,
  } = pageData;

  const renderSection = (section: string) => {
    switch (section) {
      case "intro":
        if (!introSection || !introSection.show) return null;
        return (
          <div
            className={
              introSection.containerClass ||
              "mod_intro container viewport option-1 wide wow fadeInUp"
            }
            data-s3-module
            style={{
              backgroundColor: introSection.backgroundColor,
              color: introSection.textColor,
            }}
          >
            <div className="row ">
              {introSection.logo && (
                <picture className="logo">
                  <img
                    src={getImageUrl(introSection.logo)}
                    width={introSection.logoWidth || 58}
                    height={introSection.logoHeight || 64}
                    loading="lazy"
                    alt={introSection.logoAlt || "Goldfingers aesthetic logo"}
                    draggable="false"
                    data-api-key="logo"
                  />
                </picture>
              )}
              <div className="inner" data-api-key="content">
                {introSection.heading && (
                  <div
                    style={{ color: introSection.headingColor }}
                    dangerouslySetInnerHTML={{
                      __html: lexicalToHtml(introSection.heading),
                    }}
                  />
                )}
              </div>
            </div>
          </div>
        );

      case "duration":
        if (!durationSection || !durationSection.show) return null;
        return (
          <div
            className={
              durationSection.containerClass || "mod_column viewport container"
            }
            data-s3-module
            style={{
              backgroundColor: durationSection.backgroundColor,
              color: durationSection.textColor,
            }}
          >
            <div className="row " data-api-key="content">
              <div className="item">
                <center>
                  {durationSection.durationLabel && (
                    <h3 style={{ color: durationSection.labelColor }}>
                      {durationSection.durationLabel}
                    </h3>
                  )}
                  {durationSection.durationValue && (
                    <h4>
                      <em style={{ color: durationSection.valueColor }}>
                        {durationSection.durationValue}
                      </em>
                    </h4>
                  )}
                  {durationSection.destinationsLabel && (
                    <h3 style={{ color: durationSection.labelColor }}>
                      {durationSection.destinationsLabel}
                    </h3>
                  )}
                  {durationSection.destinations &&
                    durationSection.destinations.length > 0 && (
                      <h4>
                        {durationSection.destinations.map(
                          (dest: any, index: number) => (
                            <em
                              key={index}
                              style={{ color: durationSection.valueColor }}
                            >
                              {dest.location}
                              {index <
                                durationSection.destinations.length - 1 &&
                                " Â· "}
                            </em>
                          )
                        )}
                      </h4>
                    )}
                </center>
              </div>
            </div>
          </div>
        );

      case "twoColumn":
        if (!twoColumnSection || !twoColumnSection.show) return null;
        const rightImage = twoColumnSection.rightImage?.image
          ? getImageUrl(twoColumnSection.rightImage.image)
          : null;

        return (
          <div
            className={
              twoColumnSection.containerClass ||
              "mod_columns_two viewport container"
            }
            data-s3-module
            style={{
              backgroundColor: twoColumnSection.backgroundColor,
              color: twoColumnSection.textColor,
            }}
          >
            <div className="row ">
              <div
                className={`content ${
                  twoColumnSection.verticalAlign ? "vertical-align" : ""
                }`}
              >
                <div
                  className={`item wow ${
                    twoColumnSection.leftAnimation || "fadeInLeft"
                  }`}
                  data-api-key="content_left"
                >
                  {twoColumnSection.leftContent && (
                    <div
                      style={{ color: twoColumnSection.leftContentColor }}
                      dangerouslySetInnerHTML={{
                        __html: lexicalToHtml(twoColumnSection.leftContent),
                      }}
                    />
                  )}
                </div>
                <div
                  className={`item wow ${
                    twoColumnSection.rightAnimation || "fadeInRight"
                  }`}
                  data-api-key="content_right"
                >
                  {rightImage && (
                    <p>
                      <img
                        alt={
                          twoColumnSection.rightImage.alt ||
                          "Patient looking in mirror following injection"
                        }
                        src={rightImage}
                      />
                    </p>
                  )}
                </div>
              </div>
            </div>
          </div>
        );

      case "threeColumn":
        if (!threeColumnSection || !threeColumnSection.show) return null;
        const threeColBgImages = threeColumnSection.backgroundImage
          ? getResponsiveImageUrls(threeColumnSection.backgroundImage)
          : null;

        return (
          <div
            className={
              threeColumnSection.containerClass ||
              "mod_columns_three viewport container"
            }
            data-s3-module
            style={{
              backgroundColor: threeColumnSection.backgroundColor,
              color: threeColumnSection.textColor,
            }}
          >
            <div className="row ">
              <div className="content">
                {threeColumnSection.columns &&
                  threeColumnSection.columns.map(
                    (column: any, index: number) => {
                      const dataKey =
                        index === 0
                          ? "content_left"
                          : index === 1
                          ? "content_center"
                          : "content_right";
                      return (
                        <div
                          key={index}
                          className={`wow ${column.animation || "fadeInLeft"}`}
                          data-api-key={dataKey}
                        >
                          {column.heading && (
                            <h1 style={{ color: column.headingColor }}>
                              {column.heading}
                            </h1>
                          )}
                          {column.bulletPoints &&
                            column.bulletPoints.length > 0 && (
                              <ul>
                                {column.bulletPoints.map(
                                  (bullet: any, bIndex: number) => (
                                    <li key={bIndex}>
                                      {bullet.text}
                                      {bullet.subBullets &&
                                        bullet.subBullets.length > 0 && (
                                          <ul>
                                            {bullet.subBullets.map(
                                              (
                                                subBullet: any,
                                                sIndex: number
                                              ) => (
                                                <li key={sIndex}>
                                                  {subBullet.text}
                                                  {subBullet.nestedBullets &&
                                                    subBullet.nestedBullets
                                                      .length > 0 && (
                                                      <ul>
                                                        {subBullet.nestedBullets.map(
                                                          (
                                                            nested: any,
                                                            nIndex: number
                                                          ) => (
                                                            <li key={nIndex}>
                                                              {nested.text}
                                                            </li>
                                                          )
                                                        )}
                                                      </ul>
                                                    )}
                                                </li>
                                              )
                                            )}
                                          </ul>
                                        )}
                                    </li>
                                  )
                                )}
                              </ul>
                            )}
                        </div>
                      );
                    }
                  )}
              </div>
            </div>
            {threeColBgImages && (
              <picture className="background">
                <source
                  media="(max-width:420px)"
                  srcSet={threeColBgImages.mobile}
                />
                <source
                  media="(max-width:800px)"
                  srcSet={threeColBgImages.tablet}
                />
                <source
                  media="(max-width:1400px)"
                  srcSet={threeColBgImages.desktop}
                />
                <source srcSet={threeColBgImages.large} />
                <img
                  src={threeColBgImages.large}
                  loading="lazy"
                  alt={threeColBgImages.alt || ""}
                  draggable="false"
                />
              </picture>
            )}
          </div>
        );

      case "boxGrid":
        if (!boxGridSection || !boxGridSection.show) return null;
        const boxGridBgImages = boxGridSection.backgroundImage
          ? getResponsiveImageUrls(boxGridSection.backgroundImage)
          : null;

        return (
          <div
            className={
              boxGridSection.containerClass ||
              "mod_box_grid viewport    container option-1 default  bkg-image wider "
            }
            data-s3-module
            style={{
              backgroundColor: boxGridSection.backgroundColor,
              color: boxGridSection.textColor,
            }}
          >
            <div className="row">
              {boxGridSection.mainHeading && (
                <div className="content-before">
                  <h2 style={{ color: boxGridSection.mainHeadingColor }}>
                    {boxGridSection.mainHeading}
                  </h2>
                </div>
              )}
              <div className="boxes wider">
                {boxGridSection.boxes &&
                  boxGridSection.boxes.map((box: any, index: number) => (
                    <div
                      key={index}
                      className={`box wow ${box.animation || "fadeInUp"}`}
                      style={{
                        backgroundColor: box.backgroundColor,
                        color: box.textColor,
                      }}
                    >
                      {box.heading && (
                        <h3 style={{ color: box.headingColor }}>
                          {box.heading}
                        </h3>
                      )}
                      {box.content && (
                        <div
                          dangerouslySetInnerHTML={{
                            __html: lexicalToHtml(box.content),
                          }}
                        />
                      )}
                      {box.bulletPoints && box.bulletPoints.length > 0 && (
                        <ul>
                          {box.bulletPoints.map(
                            (bullet: any, bIndex: number) => (
                              <li key={bIndex}>
                                <div
                                  dangerouslySetInnerHTML={{
                                    __html: lexicalToHtml(bullet.text),
                                  }}
                                />
                                {bullet.subBullets &&
                                  bullet.subBullets.length > 0 && (
                                    <ul>
                                      {bullet.subBullets.map(
                                        (subBullet: any, sIndex: number) => (
                                          <li key={sIndex}>
                                            <div
                                              dangerouslySetInnerHTML={{
                                                __html: lexicalToHtml(
                                                  subBullet.text
                                                ),
                                              }}
                                            />
                                            {subBullet.nestedBullets &&
                                              subBullet.nestedBullets.length >
                                                0 && (
                                                <ul>
                                                  {subBullet.nestedBullets.map(
                                                    (
                                                      nested: any,
                                                      nIndex: number
                                                    ) => (
                                                      <li key={nIndex}>
                                                        <div
                                                          dangerouslySetInnerHTML={{
                                                            __html:
                                                              lexicalToHtml(
                                                                nested.text
                                                              ),
                                                          }}
                                                        />
                                                      </li>
                                                    )
                                                  )}
                                                </ul>
                                              )}
                                          </li>
                                        )
                                      )}
                                    </ul>
                                  )}
                              </li>
                            )
                          )}
                        </ul>
                      )}
                    </div>
                  ))}
              </div>
            </div>
            {boxGridBgImages && (
              <picture className="background">
                <source
                  media="(max-width:420px)"
                  srcSet={boxGridBgImages.mobile}
                />
                <source
                  media="(max-width:800px)"
                  srcSet={boxGridBgImages.tablet}
                />
                <source
                  media="(max-width:1400px)"
                  srcSet={boxGridBgImages.desktop}
                />
                <source srcSet={boxGridBgImages.large} />
                <img
                  src={boxGridBgImages.large}
                  loading="lazy"
                  alt={boxGridBgImages.alt || "Background media"}
                  draggable="false"
                />
              </picture>
            )}
          </div>
        );

      case "tuition":
        if (!tuitionSection || !tuitionSection.show) return null;
        return (
          <div
            className={
              tuitionSection.containerClass || "mod_column viewport container"
            }
            data-s3-module
            style={{
              backgroundColor: tuitionSection.backgroundColor,
              color: tuitionSection.textColor,
            }}
          >
            <div className="row " data-api-key="content">
              <div className="item">
                <center>
                  {tuitionSection.heading && (
                    <h1 style={{ color: tuitionSection.headingColor }}>
                      {tuitionSection.heading}
                    </h1>
                  )}
                  {tuitionSection.priceText && (
                    <h4 style={{ color: tuitionSection.priceColor }}>
                      {tuitionSection.priceText}
                    </h4>
                  )}
                  {tuitionSection.description && (
                    <h4 style={{ color: tuitionSection.descriptionColor }}>
                      <div
                        dangerouslySetInnerHTML={{
                          __html: lexicalToHtml(tuitionSection.description),
                        }}
                      />
                    </h4>
                  )}
                  {tuitionSection.additionalNotes &&
                    tuitionSection.additionalNotes.map(
                      (note: any, index: number) => (
                        <p key={index} style={{ color: note.textColor }}>
                          <div
                            dangerouslySetInnerHTML={{
                              __html: lexicalToHtml(note.text),
                            }}
                          />
                        </p>
                      )
                    )}
                  <p></p>
                  {tuitionSection.ctaButton?.show && (
                    <p>
                      <a
                        href={tuitionSection.ctaButton.url || "#"}
                        target={
                          tuitionSection.ctaButton.openInNewTab
                            ? "_blank"
                            : "_self"
                        }
                        className={tuitionSection.ctaButton.className || "btn"}
                        rel={
                          tuitionSection.ctaButton.openInNewTab
                            ? "noopener"
                            : undefined
                        }
                        style={{
                          backgroundColor:
                            tuitionSection.ctaButton.backgroundColor,
                          color: tuitionSection.ctaButton.textColor,
                        }}
                        onMouseOver={(e) =>
                          tuitionSection.ctaButton?.hoverBackgroundColor &&
                          (e.currentTarget.style.backgroundColor =
                            tuitionSection.ctaButton.hoverBackgroundColor)
                        }
                        onMouseOut={(e) =>
                          tuitionSection.ctaButton?.backgroundColor &&
                          (e.currentTarget.style.backgroundColor =
                            tuitionSection.ctaButton.backgroundColor)
                        }
                      >
                        {tuitionSection.ctaButton.text || "APPLY HERE"}
                      </a>
                    </p>
                  )}
                </center>
              </div>
            </div>
          </div>
        );

      default:
        return null;
    }
  };

  return (
    <main id="main">
      {/* Page Layout */}
      {sectionOrder &&
        sectionOrder.map((item, index) => (
          <React.Fragment key={index}>
            {renderSection(item.section)}
          </React.Fragment>
        ))}
      {/* /Page Layout */}
    </main>
  );
};

export default ComprehensiveProgram;