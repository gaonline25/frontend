import React from "react";
import Link from "next/link";
import { getImageUrl } from "@/lib/api/OurFoundersApi";

interface FoundersProps {
  pageData: any;
}

const OurFounders: React.FC<FoundersProps> = ({ pageData }) => {
  if (!pageData) return <div>Unable to load content</div>;

  const heroSection = pageData.heroSection || {};
  const introSection = pageData.introSection || {};
  const halfImageSection = pageData.halfImageSection || {};

  const getImageWithFallback = (imageField: any, fallbackUrl: string) =>
    getImageUrl(imageField?.image) || imageField?.imageUrl || fallbackUrl;

  const sortedBreadcrumbs =
    heroSection.breadcrumbs?.items
      ?.filter((item: any) => item.isActive)
      .sort((a: any, b: any) => a.order - b.order) || [];

  const sortedIntroParagraphs =
    introSection.paragraphs
      ?.filter((p: any) => p.isActive)
      .sort((a: any, b: any) => a.order - b.order) || [];

  const sortedHalfImageParagraphs =
    halfImageSection.contentContainer?.paragraphs
      ?.filter((p: any) => p.isActive)
      .sort((a: any, b: any) => a.order - b.order) || [];

  return (
    <main id="main">
      {/* Hero Section */}
      {heroSection.isActive && (
        <div
          className={heroSection.className || "mod_hero banner mobile-stack"}
          data-s3-module=""
        >
          <div className="wrapper">
            {/* Background Image */}
            {heroSection.backgroundImage?.show && (
              <picture className="background">
                <img
                  src={`${getImageWithFallback(
                    heroSection.backgroundImage,
                    "https://www.datocms-assets.com/105497/1694632205-banner-left-with-text.jpg"
                  )}?auto=format,compress&w=1994`}
                  width={heroSection.backgroundImage?.width || 1994}
                  height={heroSection.backgroundImage?.height || 1206}
                  alt={heroSection.backgroundImage?.alt || "Background Texture"}
                  loading="eager"
                  draggable="false"
                />
              </picture>
            )}

            {/* Breadcrumbs */}
            <div className="row">
              <div className="inner">
                {heroSection.breadcrumbs?.show &&
                  sortedBreadcrumbs.length > 0 && (
                    <ul
                      className={
                        heroSection.breadcrumbs?.className ||
                        "partial_breadcrumb"
                      }
                      data-s3-partial=""
                    >
                      {sortedBreadcrumbs.map((item: any, index: number) => (
                        <li key={index}>
                          <Link
                            href={item.link || "#"}
                            role="link"
                            aria-label={
                              item.ariaLabel ||
                              "Breadcrumb navigation for Our Founders"
                            }
                          >
                            {item.text}
                          </Link>
                        </li>
                      ))}
                    </ul>
                  )}
                {React.createElement(
                  heroSection.heading?.tag || "h1",
                  {},
                  heroSection.heading?.text || "Our Founders"
                )}
              </div>
            </div>

            {/* Main Hero Image */}
            {heroSection.mainImage?.show && (
              <div className="media">
                <picture className="background">
                  <img
                    src={`${getImageWithFallback(
                      heroSection.mainImage,
                      "https://www.datocms-assets.com/105497/1694800297-our-founders-banner.jpg"
                    )}?auto=format,compress&w=1920`}
                    width={heroSection.mainImage?.width || 1920}
                    height={heroSection.mainImage?.height || 1206}
                    alt={
                      heroSection.mainImage?.alt ||
                      "Dr. James Young & Woman at Goldfingers Aesthetics"
                    }
                    draggable="false"
                  />
                </picture>
              </div>
            )}
          </div>
        </div>
      )}

      {/* Intro Section */}
      {introSection.isActive && (
        <div
          className={
            introSection.className ||
            "mod_intro container viewport option-1 wide wow fadeInUp"
          }
          data-s3-module=""
        >
          <div className="row pb-0">
            {introSection.logo?.show && (
              <picture className="logo">
                <img
                  src={getImageWithFallback(
                    introSection.logo,
                    "https://www.datocms-assets.com/105497/1692813243-logo-module.svg"
                  )}
                  width={introSection.logo?.width || 58}
                  height={introSection.logo?.height || 64}
                  alt={introSection.logo?.alt || "Goldfingers Aesthetics Logo"}
                  loading="lazy"
                  draggable="false"
                />
              </picture>
            )}
            <div className="inner">
              {React.createElement(
                introSection.heading?.tag || "h2",
                {},
                <>
                  {introSection.heading?.text?.split(
                    "Aesthetic Care Across Florida"
                  )[0] || "Pioneering Excellence in "}
                  <span className="block-desktop">
                    {introSection.heading?.text?.includes(
                      "Aesthetic Care Across Florida"
                    )
                      ? "Aesthetic Care Across Florida"
                      : introSection.heading?.text?.substring(
                          introSection.heading?.text?.indexOf(" ", 20)
                        ) || "Aesthetic Care Across Florida"}
                  </span>
                </>
              )}
              {sortedIntroParagraphs.map((paragraph: any, index: number) => (
                <p key={index}>{paragraph.text}</p>
              ))}
            </div>
          </div>
        </div>
      )}

      {/* Half Image Section */}
      {halfImageSection.isActive && (
        <div
          className={
            halfImageSection.className ||
            "mod_half_image viewport half-image container wow fadeInLeft"
          }
          data-s3-module=""
        >
          <div className="row half-image-row">
            <div
              className={halfImageSection.imageContainer?.className || "media"}
            >
              <picture className="background">
                <img
                  src={`${getImageWithFallback(
                    halfImageSection.imageContainer,
                    "https://www.datocms-assets.com/105497/1694799633-our-founders.jpg"
                  )}?auto=format,compress&w=1832`}
                  width={halfImageSection.imageContainer?.width || 1832}
                  height={halfImageSection.imageContainer?.height || 1824}
                  alt={
                    halfImageSection.imageContainer?.alt ||
                    "Our Founders Team"
                  }
                  loading="lazy"
                  draggable="false"
                />
              </picture>
            </div>
            <div
              className={
                halfImageSection.contentContainer?.className || "copy"
              }
            >
              <div className="inner">
                <div className="content">
                  {React.createElement(
                    halfImageSection.contentContainer?.heading?.tag || "h2",
                    {},
                    <>
                      {halfImageSection.contentContainer?.heading?.smallText
                        ?.show && (
                        <span
                          className={
                            halfImageSection.contentContainer?.heading
                              ?.smallText?.className || "small"
                          }
                        >
                          {
                            halfImageSection.contentContainer?.heading
                              ?.smallText?.text
                          }
                        </span>
                      )}
                      {halfImageSection.contentContainer?.heading?.text?.split(
                        "Happens Every Day."
                      )[0] || "Where Beauty "}
                      <span className="block-desktop">
                        {halfImageSection.contentContainer?.heading?.text?.includes(
                          "Happens Every Day."
                        )
                          ? "Happens Every Day."
                          : halfImageSection.contentContainer?.heading?.text?.substring(
                              halfImageSection.contentContainer?.heading?.text?.indexOf(
                                " ",
                                10
                              )
                            ) || "Happens Every Day."}
                      </span>
                    </>
                  )}
                  {sortedHalfImageParagraphs.map(
                    (paragraph: any, index: number) => (
                      <p key={index}>{paragraph.text}</p>
                    )
                  )}
                </div>
              </div>
            </div>
          </div>
        </div>
      )}
    </main>
  );
};

export default OurFounders;
