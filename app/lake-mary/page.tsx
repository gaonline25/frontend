import LakeMaryContent from "@/components/location/LakeMaryContent";
import { fetchLakeMaryPageData } from "@/lib/api/locations/fetchLakeMaryPageComponent";
import type { Metadata } from "next";

export const revalidate = 60; // ✅ ISR: Regenerate page every 60s

// ✅ Server-side CMS data fetch
async function getData() {
  try {
    const data = await fetchLakeMaryPageData();
    return data || null;
  } catch (error) {
    console.error("❌ Error fetching Lake Mary page data:", error);
    return null;
  }
}

// ✅ Dynamic SEO metadata generation
export async function generateMetadata(): Promise<Metadata> {
  const data = await getData();
  const seo = (data as any)?.seoSettings || {};

  return {
    title:
      seo.metaTitle ||
      "Goldfingers Aesthetics Lake Mary | Botox, Fillers & Med Spa Experts",
    description:
      seo.metaDescription ||
      "Goldfingers Aesthetics Lake Mary offers professional Botox, fillers, and skin rejuvenation treatments from trusted experts in Florida.",
    openGraph: {
      title:
        seo.metaTitle ||
        "Goldfingers Aesthetics Lake Mary | Botox & Med Spa Experts",
      description:
        seo.metaDescription ||
        "Discover Goldfingers Aesthetics Lake Mary — your destination for Botox, fillers, and advanced med spa treatments in Central Florida.",
      images: [
        {
          url:
            seo.ogImage?.url ||
            "https://res.cloudinary.com/dk3v64cs6/image/upload/v1700000000/default-og.jpg",
          width: 1200,
          height: 630,
          alt: "Goldfingers Aesthetics Lake Mary Med Spa",
        },
      ],
    },
    keywords:
      seo.keywords ||
      "Lake Mary med spa, Botox Lake Mary, Fillers Lake Mary, Goldfingers Aesthetics, skincare, medspa near me, Florida aesthetics",
    robots: seo.robots || "index, follow",
    alternates: { canonical: seo.canonicalUrl },
  };
}

// ✅ Server-rendered page entry
export default async function LakeMaryPage() {
  const data = await getData();

  if (!data) {
    return (
      <main className="flex items-center justify-center min-h-[60vh]">
        <p className="text-gray-500 text-center">
          Unable to load Lake Mary page content.
        </p>
      </main>
    );
  }

  // ✅ Pass fetched CMS data to client component
  return <LakeMaryContent data={data} />;
}
