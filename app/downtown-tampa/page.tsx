import type { Metadata } from "next";

export const revalidate = 60; // ✅ Regenerate every 60s

// ✅ Securely fetch CMS data on the server
async function getData() {
  try {
    const data = await fetchDowntownTampaPageData();
    return data || null;
  } catch (error) {
    console.error("❌ Error fetching Downtown Tampa page data:", error);
    return null;
  }
}

