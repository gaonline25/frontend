const PAYLOAD_API_URL =
  process.env.NEXT_PUBLIC_PAYLOAD_API_URL || "http://localhost:3001/api";

async function fetchSarasotaLocationPageComponent() {
  try {
    const res = await fetch(
      `${PAYLOAD_API_URL}/sarasota-location-page-component?where[isActive][equals]=true&depth=2&limit=100&draft=false&trash=false`,
      {
        cache: "force-cache",
      }
    );
    if (!res.ok) {
      throw new Error(`Failed to fetch Sarasota page data: ${res.statusText}`);
    }
    const data = await res.json();
    const activeComponent =
      data.docs?.find((component: any) => component.isActive) || null;
    console.log("Fetched Sarasota data:", activeComponent);
    return activeComponent;
  } catch (error) {
    console.error("Error fetching Sarasota page data:", error);
    return null;
  }
}

