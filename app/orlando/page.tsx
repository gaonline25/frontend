// ✅ Server-side CMS data fetch
async function getData() {
  try {
    const data = await fetchOrlandoLocationPageComponent();
    return data || null;
  } catch (error) {
    console.error("❌ Error fetching Orlando location page data:", error);
    return null;
  }
}

