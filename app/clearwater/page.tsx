import ClearwaterPageComponent from "@/components/location/ClearwaterPageComponent";
import { fetchClearwaterPageData } from "@/lib/api/locations/fetchClearwaterPageData";
import type { Metadata } from "next";

export const revalidate = 60; // ✅ ISR (rebuild every 60s)

// ✅ Secure server-side fetch
async function getData() {
  try {
    const data = await fetchClearwaterPageData();
    return data || null;
  } catch (error) {
    console.error("❌ Error fetching Clearwater page data:", error);
    return null;
  }
}

// ✅ Dynamic SEO from CMS
export async function generateMetadata(): Promise<Metadata> {
  const data = await getData();
  const seo = (data as any)?.seoSettings || {};

  return {
    title:
      seo.metaTitle ||
      "Goldfingers Aesthetics Clearwater | Botox, Fillers & Med Spa",
    description:
      seo.metaDescription ||
      "Visit Goldfingers Aesthetics Clearwater for Botox, fillers, skincare, and plastic surgery. Award-winning med spa treatments near the beach.",
    openGraph: {
      title:
        seo.metaTitle ||
        "Goldfingers Aesthetics Clearwater | Rejuvenation & Med Spa",
      description:
        seo.metaDescription ||
        "Book your appointment at our Clearwater med spa and experience luxury aesthetic care including Botox, fillers, and skincare.",
      images: [
        {
          url:
            seo.ogImage?.url ||
            "https://res.cloudinary.com/dk3v64cs6/image/upload/v1700000000/default-og.jpg",
          width: 1200,
          height: 630,
          alt: "Goldfingers Aesthetics Clearwater location",
        },
      ],
    },
    keywords:
      seo.keywords ||
      "Clearwater med spa, Botox Clearwater, Fillers Clearwater, Goldfingers Aesthetics, skincare, plastic surgery",
    robots: seo.robots || "index, follow",
    alternates: { canonical: seo.canonicalUrl },
  };
}

// ✅ Server-side page
export default async function Clearwater() {
  const data = await getData();

  if (!data) {
    return (
      <main className="flex items-center justify-center min-h-[60vh]">
        <p className="text-gray-500 text-center">
          Unable to load Clearwater page content.
        </p>
      </main>
    );
  }

  // ✅ Pass fetched data into client component
  return <ClearwaterPageComponent data={data} />;
}
